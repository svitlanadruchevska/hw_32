"use strict";

const add = function (a, b) {
  return a + b;
};

const subtract = function (a, b) {
  return a - b;
};

const multiply = function (a, b) {
  return a * b;
};

console.log("Сума: " + add(7, 4));
console.log("Віднімання: " + subtract(7, 4));
console.log("Множення: " + multiply(7, 4));
